## 4. Crie um algoritmo que solicite ao usuário uma cor (vermelho, verde, azul) e exiba uma mensagem correspondente à cor escolhida.

cor = str(input('Escolha uma cor entre Vermelho, Verde e Azul \n'))

if cor == 'vermelho':
    print('A COR ESCOLHIDA FOI vermelho')
elif cor == 'verde':
    print('A COR ESCOLHIDA FOI verde')
if cor == 'azul':
    print('A COR ESCOLHIDA FOI azul')
else:
    print('Não escolheu solicitada')





    

